# Install Dart: https://dart.dev/get-dart

# Activate bullseye2d
dart pub global activate bullseye2d

# Create new project
bullseye2d create hello_world
cd hello_world

# Run on web (http://localhost:8080)
bullseye2d run web

# Run on desktop (SDL3)
bullseye2d run sdl3

